function bindInteractions() {
  document.addEventListener("click", (event) => {
    const thumb = event.target.closest("[data-thumb]");
    if (thumb) document.querySelector("#mainImage").src = thumb.dataset.thumb;

    const chip = event.target.closest(".chip");
    if (chip) {
      chip.parentElement.querySelectorAll(".chip").forEach((item) => item.classList.remove("active"));
      chip.classList.add("active");
    }

    const qty = event.target.closest("[data-quantity]");
    if (qty) {
      const value = qty.querySelector("span");
      const current = Number(value.textContent);
      if (event.target.matches("[data-plus]")) value.textContent = current + 1;
      if (event.target.matches("[data-minus]")) value.textContent = Math.max(1, current - 1);
    }

    const addCart = event.target.closest("[data-add-cart]");
    if (addCart) {
      addToCart(addCart.dataset.addCart);
      addCart.textContent = "Added";
      window.setTimeout(() => {
        addCart.textContent = "Add to Cart";
      }, 900);
    }

    const removeCart = event.target.closest("[data-remove-cart]");
    if (removeCart) removeFromCart(removeCart.dataset.removeCart);

    const qtyButton = event.target.closest("[data-cart-qty]");
    if (qtyButton) changeCartQty(qtyButton.dataset.cartQty, Number(qtyButton.dataset.delta));
  });

  bindButtonRipples();
}

function bindButtonRipples() {
  document.querySelectorAll(".btn").forEach((button) => {
    if (button.dataset.rippleBound) return;
    button.dataset.rippleBound = "true";
    button.addEventListener("pointermove", (event) => {
      const rect = button.getBoundingClientRect();
      button.style.setProperty("--x", `${event.clientX - rect.left}px`);
      button.style.setProperty("--y", `${event.clientY - rect.top}px`);
    });
  });
}

function revealOnScroll() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) entry.target.classList.add("visible");
    });
  }, { threshold: 0.12 });
  document.querySelectorAll(".reveal").forEach((item) => observer.observe(item));
}

function initCheckout() {
  const form = document.querySelector("[data-checkout-form]");
  if (!form) return;

  form.addEventListener("submit", (event) => {
    event.preventDefault();
    const message = document.querySelector("[data-checkout-message]");
    const submitButton = form.querySelector("button[type='submit']");
    const data = Object.fromEntries(new FormData(form).entries());
    const fullName = `${data.first_name || ""} ${data.last_name || ""}`.trim();
    const setMessage = (text, type = "") => {
      if (!message) return;
      message.textContent = text;
      message.dataset.status = type;
    };

    const setLoading = (isLoading) => {
      if (!submitButton) return;
      submitButton.disabled = isLoading;
      submitButton.textContent = isLoading ? "Opening WhatsApp..." : "Place Order on WhatsApp";
    };

    updateCheckoutSummary();
    const summaryForOrder = cartSummary();
    if (summaryForOrder.total <= 0) {
      setMessage("Your cart is empty. Add a product before placing an order.", "error");
      return;
    }

    const orderText = [
      "Hi, I want to place this 3D printing order.",
      `Name: ${fullName || "Customer"}`,
      `Phone: ${data.phone || ""}`,
      `Address: ${data.address || ""}, ${data.city || ""} ${data.postal_code || ""}`.trim(),
      `Products: ${summaryForOrder.items}`,
      `Product price total: ${formatPrice(summaryForOrder.total)}`,
      codNote,
      shippingNote,
      "Please confirm the final shipping charge for my location."
    ].join("\n");

    setLoading(true);
    setMessage("Opening WhatsApp with your order details.", "success");
    window.open(`https://wa.me/${storefrontSettings.whatsapp_number || "638326318"}?text=${encodeURIComponent(orderText)}`, "_blank", "noopener");
    setLoading(false);
  });
}

function initTracking() {
  const form = document.querySelector("[data-track-form]");
  if (!form) return;

  const result = document.querySelector("[data-tracking-result]");
  const input = form.querySelector("input[name='tracking_code']");
  const urlCode = new URLSearchParams(location.search).get("order");
  if (urlCode) input.value = urlCode;

  const render = (data) => {
    result.innerHTML = `
      <div class="tracking-card">
        <p class="eyebrow">${data.tracking_code || input.value}</p>
        <h3>${data.status_label || "Order received"}</h3>
        <p>${data.detail || "Your order is confirmed. We will update the print and delivery status soon."}</p>
        <div class="tracking-steps">
          <span class="active">Ordered</span>
          <span class="${["accepted", "paid", "processing", "shipped", "delivered"].includes(data.status) ? "active" : ""}">Accepted</span>
          <span class="${["processing", "shipped", "delivered"].includes(data.status) ? "active" : ""}">Printing</span>
          <span class="${["shipped", "delivered"].includes(data.status) ? "active" : ""}">Shipped</span>
        </div>
      </div>`;
  };

  form.addEventListener("submit", async (event) => {
    event.preventDefault();
    const code = input.value.trim();
    if (!code) return;

    render({ tracking_code: code, status: "processing", status_label: "Tracking updates on WhatsApp", detail: "The new Django backend stores order status, payment, and shipment details. Please message us on WhatsApp for live support." });
  });

  if (urlCode) form.dispatchEvent(new Event("submit"));
}

