function getCart() {
  try {
    return JSON.parse(localStorage.getItem(cartStorageKey)) || [];
  } catch (error) {
    return [];
  }
}

function saveCart(cart) {
  localStorage.setItem(cartStorageKey, JSON.stringify(cart));
}

function cartCount() {
  return getCart().reduce((total, item) => total + Number(item.qty || 1), 0);
}

function updateCartCount() {
  const count = cartCount();
  document.querySelectorAll("[data-cart-count]").forEach((badge) => {
    badge.textContent = count;
    badge.hidden = count === 0;
  });
}

function addToCart(productId) {
  const id = Number(productId);
  const product = products.find((item) => Number(item.id) === id);
  if (!product) return;
  const cart = getCart();
  const existing = cart.find((item) => Number(item.id) === id);

  if (existing) {
    existing.qty = Number(existing.qty || 1) + 1;
  } else {
    cart.push({ id, qty: 1 });
  }

  saveCart(cart);
  updateCartCount();
  renderCart();
  updateCheckoutSummary();
}

function removeFromCart(productId) {
  const id = Number(productId);
  saveCart(getCart().filter((item) => Number(item.id) !== id));
  updateCartCount();
  renderCart();
  updateCheckoutSummary();
}

function changeCartQty(productId, delta) {
  const id = Number(productId);
  const cart = getCart().map((item) => {
    if (Number(item.id) !== id) return item;
    return { ...item, qty: Math.max(1, Number(item.qty || 1) + delta) };
  });
  saveCart(cart);
  updateCartCount();
  renderCart();
  updateCheckoutSummary();
}

